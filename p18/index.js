// 1. Menggunakan function declaration
function halo() {
  console.log("Halo!");
}
halo();

// 2. Function dengan parameter
function sapa(nama) {
  console.log("Halo " + nama);
}
sapa("Ebren Tinambunan");

// 3. Function dengan return
function tambah(a, b) {
  return a + b;
}
let hasil = tambah(3, 4);
console.log(hasil);
